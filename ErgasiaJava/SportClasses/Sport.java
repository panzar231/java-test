package ErgasiaJava.SportClasses;

public class Sport {
    private String sportName;
    private boolean onlyProffesional;

    public String getSportName() {
        return sportName;
    }

    public void setSportName(String sportName) {
        this.sportName = sportName;
    }

    public boolean isOnlyProffesional() {
        return onlyProffesional;
    }

    public void setOnlyProffesional(boolean onlyProffesional) {
        this.onlyProffesional = onlyProffesional;
    }

}